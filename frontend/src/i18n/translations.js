export function getTranslation(lang, key) {
  const parts = key.split('.');
  let cur = translations[lang] || translations.uz;
  for (const p of parts) {
    if (cur && typeof cur === 'object' && p in cur) cur = cur[p];
    else return key;
  }
  return cur;
}
